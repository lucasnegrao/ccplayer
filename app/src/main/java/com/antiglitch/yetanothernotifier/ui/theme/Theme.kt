// --- Theme Composable ---
@SuppressLint("NewApi")
@Composable
fun YetAnotherNotifierTheme(
    isDarkTheme: Boolean = true,//isSystemInDarkTheme(),
    isDynamicColor: Boolean = false,
    content: @Composable () -> Unit
) {
    val dynamicColor = isDynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S
    val colorScheme = when {
        dynamicColor && isDarkTheme -> dynamicDarkColorScheme(LocalContext.current)
        dynamicColor && !isDarkTheme -> dynamicLightColorScheme(LocalContext.current)
        isDarkTheme -> YetAnotherNotifierDarkColorScheme
        else -> YetAnotherNotifierLightColorScheme
    }

    MaterialTheme(
        colorScheme = colorScheme,
        // typography = YetAnotherNotifierTypography, // Define if you have custom typography
        content = content,
    )
}